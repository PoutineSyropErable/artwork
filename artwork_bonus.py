def polygon(x,y,n,l,R=97,G=97,B=97):
    n = int(abs(n)) 

    
    polygon= turtle.Turtle()
    polygon.speed(10)
    turtle.colormode(255)
    polygon.color(29,29,29)

    
    polygon.penup()
    polygon.goto(x,y)
    set_angle(polygon,x,y,0)
    #polygon.setheading(0)

    
    r_inner = l*0.5/math.tan(math.pi/n) #This works exactly for even n
    #The exact answer is horrible for odd n. You can probably google it
    #But I went through the pain of doing it all on paper mathematically.
    #geometric Series of e^ibn.  
    polygon.forward(r_inner)
    
    polygon.pendown()
    polygon.begin_fill()
    polygon.fillcolor(R,G,B)
    set_angle(polygon,x + r_inner,y,90)
    polygon.pendown() #---Because set_angle has a pen_up at the end. 
    #polygon.setheading(90)
    
    polygon.forward(l/2)
    polygon.left(360/n)
    
    for i in range(n-1):
        polygon.forward(l)
        polygon.left(360/n)
        
    polygon.forward(l/2)
    polygon.end_fill()
    polygon.hideturtle()
        
